package br.samirmelo.tech.composicao;

public class Profissao {
	private String medico;
	private String engenheiro;
	private String programador;
	private String professor;
	
	
	public String getMedico() {
		return medico;
	}
	public void setMedico(String medico) {
		this.medico = medico;
	}
	public String getEngenheiro() {
		return engenheiro;
	}
	public void setEngenheiro(String engenheiro) {
		this.engenheiro = engenheiro;
	}
	public String getProgramador() {
		return programador;
	}
	public void setProgramador(String programador) {
		this.programador = programador;
	}
	public String getProfessor() {
		return professor;
	}
	public void setProfessor(String professor) {
		this.professor = professor;
	}
	
	
	
}
